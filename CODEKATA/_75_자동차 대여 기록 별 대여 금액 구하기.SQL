WITH TRUCKS AS (
    SELECT C.CAR_ID, CAR_TYPE, HISTORY_ID, DAILY_FEE, DATEDIFF(END_DATE, START_DATE) + 1 AS DURATION
    FROM CAR_RENTAL_COMPANY_CAR AS C
             JOIN CAR_RENTAL_COMPANY_RENTAL_HISTORY AS H
                  ON C.CAR_ID = H.CAR_ID
    WHERE CAR_TYPE = '트럭'
)

SELECT HISTORY_ID, FLOOR((DAILY_FEE * DURATION) * (1 - (IFNULL(D.DISCOUNT_RATE, 0) / 100))) AS FEE
FROM TRUCKS AS T
         LEFT JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN AS D
                   ON T.CAR_TYPE = D.CAR_TYPE AND
                      IF(T.DURATION BETWEEN 7 AND 29, '7일 이상',
                         IF(T.DURATION BETWEEN 30 AND 89, '30일 이상',
                            IF(90 <= T.DURATION, '90일 이상','없음'))) = D.DURATION_TYPE
ORDER BY 2 DESC, 1 DESC