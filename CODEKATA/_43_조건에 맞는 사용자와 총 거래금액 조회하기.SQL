SELECT *
FROM
    (SELECT B.WRITER_ID, NICKNAME, SUM(PRICE) AS TOTAL_SALES
     FROM USED_GOODS_BOARD AS B
              JOIN USED_GOODS_USER AS U
                   ON B.WRITER_ID = U.USER_ID
     WHERE STATUS = 'DONE'
     GROUP BY B.WRITER_ID
     ORDER BY TOTAL_SALES
    ) AS T
WHERE TOTAL_SALES >= 700000